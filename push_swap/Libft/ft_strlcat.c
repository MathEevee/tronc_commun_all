#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	len_dst;
	size_t	len_src;
	size_t	i;
	size_t	j;

	if (dst == NULL && size == 0)
		return (0);
	i = ft_strlen(dst);
	j = 0;
	len_dst = i;
	len_src = ft_strlen(src);
	if (size <= len_dst)
		return (len_src + size);
	while (src[j] && i < size - 1)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (len_dst + len_src);
}
